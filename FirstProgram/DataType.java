package FirstProgram;

public class DataType {

	public static void main(String[] args) {
		byte a = 10;
		short b = 20;
		int c = 30;
		long d = 1000;
		float e = 10.12f;
		double f =100.200;
		char g = '4';
		boolean h = true;
		System.out.println("Byte value of a = " + a);       // + is a concatenation symbol
		System.out.println("Short value of b = " + b);
		System.out.println("int value of c = " + c);
		System.out.println("long value of d = " + d);
		System.out.println("Float value of e = " + e);
		System.out.println("Double value of f = " + f);
		System.out.println("Char value of g = " + g);
		System.out.println("Boolean value of h = " + h);







		
		
	}

}
