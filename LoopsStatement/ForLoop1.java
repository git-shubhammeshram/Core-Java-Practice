package LoopsStatement;

//For loop is used to iterate a part of program multiple times if number of iteration is
//fixed

public class ForLoop1 {

	public static void main(String[] args) {
		for(int i=10;i>=1;i=i-1)
		{
			System.out.print(" " + i);
		}
	}

}
//Loops : Loops are used to execute a set of instructions/ methods repeatedly when some
//conditions become true.
