package LoopsStatement;

public class WhileLoop1 {
	
	//While loop is used to iterate a part of program multiple times if number of iteration is
	//not fixed
//	while loop is preferred to use, If the number of iteration is not fixed. It is pre-test loop
//	it is also called as entry control loop
	public static void main(String[] args) {
		
		//Print 1 to 5
		int i=1; //
		while (i<=5) //Conditions
		{
			System.out.println(i);
			i++; // Increments
		}
	}

}
